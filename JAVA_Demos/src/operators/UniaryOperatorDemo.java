package operators;

/*
 * uniary operator: operate that can perfrom operations on single value
 * Pre and Post incement operator
 * Pre and Post decrement operator 
 * */
public class UniaryOperatorDemo {

	public static void main(String[] args) {
		int a = 10;
		a++;//11
		System.out.println(a);//11
		++a;//11->12
		System.out.println(a);//12

	}

}
