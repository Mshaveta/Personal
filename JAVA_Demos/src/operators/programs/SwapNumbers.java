package operators.programs;

public class SwapNumbers {

	public static void main(String[] args) {
		int a=10;//variable declaration along with init.
		int b=5;
		int c;//variable declaration
		
		System.out.println("value of and b is before swapping -"+ a+" "+b);
		
		c=a; //c=10
		a=b;//a=5
		b=c;//b=10
		
		System.out.println("value of and b is after swapping -"+ a+" "+b);
		
		
	}

}
