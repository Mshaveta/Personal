package OOPs_Concepts.Variables;

/*
 * Local variable - declares inside the method
 * initialisation is mandatory for local variable
 * local variable is having scope within method.
 */
public class LocalVariable {
	
	public void m1() {
		String b = null; //local variable
		System.out.println(b);

	}

	public static void main(String[] args) {
		int a = 0;
		System.out.println(a);
		 

	}

}
